#include <unistd.h>

int		main(void)
{
	int i;
	int a;

	i = 'a';
	while (i <= 'z')
	{
		if (i % 2 == 0)
		{
			a = i - 32;
			write(1, &a, 1);
		}
		else
			write(1, &i, 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
