#include <unistd.h>

char	*lowerup(char *c)
{
	while (*c != '\0')
	{
		if (*c >= 'a' && *c <= 'z')
			*c = *c - 32;
		else if (*c >= 'A' && *c <= 'Z')
			*c = *c + 32;
		c++;
	}
	return (c);
}

int		main(int ac, char **av)
{
	int i;

	i = 0;
	if (ac == 2)
	{
		write(1, lowerup(&av[1][i]), 1);
		i++;
	}

	write(1, "\n", 1);
	return (0);
}
