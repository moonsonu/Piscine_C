#include <stdio.h>

char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (j < nb && src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}

int		main(void)
{
	char dest1[10] = "sdffgh";
	char src1[10] = "abce";
	int c;

	c = 2;
	ft_strncat(dest1, src1, c);
	printf("%s\n", dest1);
	printf("%s", src1);
	return (0);
}

