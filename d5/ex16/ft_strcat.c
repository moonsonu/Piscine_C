#include <stdio.h>

char	*ft_strcat(char *dest, char *src)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (dest[i] != 0)
		i++;
	while (src[j] != 0)
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}

int		main(void)
{
	char src1[10] = "asdf";
	char des1[10] = "qwe";

	ft_strcat(des1, src1);
	printf("%s\n", des1);
	printf("%s", src1);
	return (0);
}
