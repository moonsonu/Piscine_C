int		ft_atoi(char *str)
{
	int a;
	int b;
	int c;

	a = 0;
	b = 0;
	c = 1;
	if (str[0] == '-')
	{
		c = -1;
		a = 1;
	}
	while (str[a] != '\0')
	{
		if (str[a] >= '0' && str[a] <= '9')
		{
			b = b * 10 + str[a] - '0';
			if (str[a + 1] <= 47 || str[a + 1] >= 58)
				return (b * c);
			if (b == 0)
				return (0);
		}
		a++;
	}
	return (b * c);
}
