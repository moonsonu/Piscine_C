int		ft_str_is_printable(char *str)
{
	int i;
	int yn;

	i = 0;
	while (str[i] != '\0')
	{
		yn = 0;
		if (str[i] >= 32 && str[i] <= 126)
			yn = 1;
		if (yn == 0)
			return (0);
		i++;
	}
	return (1);
}
