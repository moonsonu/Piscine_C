#include <stdio.h>

int		ft_sqrt(int nb)
{
	int a;
	int i;

	i = 1;
	a = 1;
	if (nb == 0 || nb == 1)
		return (nb);
	while (i * i != nb)
	{
		a = i * i;
		if (a == nb)
			return (i);
		else if (a > nb)
			return (0);
		i++;
	}
	return (0);
}
