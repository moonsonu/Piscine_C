int		*ft_map(int *tab, int length, int (*f)(int))
{
	int i;
	int *rc;

	i = 0;
	rc = (int*)malloc(sizeof(int) * length);
	while (i < length)
	{
		rc[i] = f(tab[i]);
		i++;
	}
	return (rc);
}
